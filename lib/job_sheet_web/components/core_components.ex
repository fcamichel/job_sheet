  ## JS Commands

  def show(js \\ %JS{}, selector) do
    JS.add_class(js, "!block", to: selector)
    |> JS.remove_class("hidden", to: selector)
    |> JS.add_class("opacity-100", to: selector)
  end

  def hide(js \\ %JS{}, selector) do
    JS.add_class(js, "hidden", to: selector)
    |> JS.remove_class("!block", to: selector)
    |> JS.remove_class("opacity-100", to: selector)
  end

  def show_modal(js \\ %JS{}, id) when is_binary(id) do
    js
    |> JS.add_class("!block", to: "##{id}")
    |> JS.show(to: "##{id}-bg")
    |> JS.show(to: "##{id}-container")
    |> JS.add_class("opacity-100", to: "##{id}-bg")
    |> JS.add_class("opacity-100", to: "##{id}-container")
    |> JS.focus_first(to: "##{id}-content")
  end

  def hide_modal(js \\ %JS{}, id) do
    js
    |> JS.hide(to: "##{id}-bg")
    |> JS.hide(to: "##{id}-container")
    |> JS.remove_class("opacity-100", to: "##{id}-bg")
    |> JS.remove_class("opacity-100", to: "##{id}-container")
    |> JS.add_class("hidden", to: "##{id}")
  end

  ## Translation Helpers

  defp translate_error({msg, opts}) do
    # When using gettext, we typically pass the strings we want
    # to translate as a static argument:
    #
    #     # Translate the number of files with dngettext
    #     dngettext("errors", "1 file", "%{count} files", count)
    #
    # However, the error messages in our forms and APIs are
    # typically dynamic. In these cases, we translate the message by
    # calling Gettext with the gettext backend as first argument.
    if count = opts[:count] do
      Gettext.dngettext(JobSheetWeb.Gettext, "errors", msg, msg, count, opts)
    else
      Gettext.dgettext(JobSheetWeb.Gettext, "errors", msg, opts)
    end
  end

  defp translate_error(msg), do: msg
end
